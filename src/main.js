function setTileMap(map, width, height) {
    var row = 0
    var temp = 0
    for (var i = 0; i < maps[map].length; i++) {
        for (var j = 0; j < maps[map][i].length; j++) {
            var newDiv = document.createElement("div");
            newDiv.style.height = height + 'px';
            newDiv.style.width = width + 'px';
            newDiv.className = "grid"
            newDiv.id = `${i}:${j}`
            newDiv.onclick = function() {
                if (this.style.backgroundColor == "red") {
                    this.style.backgroundColor = "white";
                } else {
                    this.style.backgroundColor = "red";
                }
            };
            //newDiv.style.transform = `translate(500px, 300px)`
            newDiv.style.transform = `translateX(${(j) * width}px) translateY(${temp + row}px)`
            var parentDiv = document.getElementById("game");
            parentDiv.appendChild(newDiv);
            temp -= height
        }
        row += height
    }
}

